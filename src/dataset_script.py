def hindi_to_hinglish(text):
    mapping = {
        "मुझे": "mujhe",
        "पानी": "pani",
        "चाहिए": "chahiye",
        "मैं": "main",
        "थक": "thak",
        "गया": "gaya",
        "हूँ": "hu",
        "आज": "aaj",
        "मौसम": "mausam",
        "अच्छा": "accha",
        "है": "hai",
        "तुम": "tum",
        "कहाँ": "kahan",
        "जल्दी": "jaldi",
        "आओ": "aao",
        "बाहर": "bahar",
        "जाना": "jana"
    }

    words = text.split()
    output = []

    for w in words:
        if w in mapping:
            output.append(mapping[w])
        else:
            # fallback transliteration
            temp = transliterate(w, DEVANAGARI, ITRANS)
            temp = temp.lower().replace("aa", "a").replace(".", "")
            output.append(temp)

    return " ".join(output)

