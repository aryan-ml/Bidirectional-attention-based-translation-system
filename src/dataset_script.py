def clean_text(text):
    text = text.lower().strip()
    text = re.sub(r'\s+', ' ', text)
    return text



def add_noise(text):
    variations = [text]

    # suffix noise
    if random.random() < 0.3:
        variations.append(text + random.choice([" yaar", " bro", " bhai", " na"]))

    # realistic short forms
    if random.random() < 0.2:
        t = text
        t = t.replace("hai", "h")
        t = t.replace("hoon", "hu")
        t = t.replace("nahi", "nhi")
        t = t.replace("raha", "rha")
        variations.append(t)

    # small typo
    if random.random() < 0.1:
        variations.append(text.replace("mujhe", "muje"))

    return random.choice(variations)


def normalize_output(text):
    text = text.strip().lower()

    # --- normalize common short forms ---
    text = text.replace(" hu", " hoon")
    text = text.replace(" h ", " hai ")
    if text.endswith(" h"):
        text = text[:-2] + " hai"

    # --- remove trailing slang (only at end) ---
    slang = [" bro", " bhai", " yaar", " na"]
    for s in slang:
        if text.endswith(s):
            text = text[: -len(s)].strip()

    return text

